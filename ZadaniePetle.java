public class ZadaniePetle {
    public static void main(String[] args) {

        //Wyświetl liczby od 1 do 10
        for(int i = 1; i < 11; i++){
            System.out.println(i);

        }
        System.out.println("----------");
        //Wyświetl liczby od 5 do 10
        for (int i = 5; i < 11; i++){
            System.out.println(i);
        }
        System.out.println("----------");
        //Wyświetl liczby od 10 do 1
        for (int i = 10; i >= 1; i--){
            System.out.println(i);
        }
        System.out.println("----------");
    }
}
